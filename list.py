# Create and access list
lst = [10, 20, 30, 40]
print("List:", lst)
print("First element:", lst[0])

# Add and remove elements
lst.append(50)
print("After adding:", lst)

lst.remove(20)
print("After removing:", lst)

# Sort list
lst.sort()
print("Sorted list:", lst)

# Reverse list
lst.reverse()
print("Reversed list:", lst)
